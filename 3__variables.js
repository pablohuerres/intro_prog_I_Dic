// VARIABLES
// def: son celdas de memoria dentro de un lenguaje de prog, que nos permite almacenar diferentes tipos de datos 

// TIPOS DE VARIABLES: 
// 1-numéricas 
var numero1 = 5; // declaro e inicializo la variable "numero1"
var numero2 = 2.5; // declaro e inicializo la variable "numero2"
numero2 = 2.6; //actualizo la variable "numero2"

// 2-texto ó String 
var saludo = "Hola!";
var despedida = 'Adios!';

// 3-Booleanas
var verdadero = true;
var falso = false;

// 4-Arrays 
var coches = ["Seat","Fiat","Renault","BMW","Audi"]; 

// OPERAR CON VARIABLES 
// +   :   SUMA 
// -   :   RESTA 
// *   :   MULTIPLICACIÓN 
// /   :   DIVISIÓN 
// %   :   RESTO 
// ++  :   INCREMENTO EN UNA UNIDAD 
// --  :   DECREMENTO EN UNA UNIDAD 

Ejemplos: 
var n1 = 10;
var n2 = 5;

var suma = n1 + n2;
var resta = n1 - n2;
var mult = n1 * n2;
var div = n1 / n2;
var resto = n1 % n2;

// Función de salida por consola (ver: punto 2 del temario)
console.log(suma);
console.log(resta);
console.log(mult);
console.log(div);
console.log(resto);